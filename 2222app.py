# برنامه تولید جدول ضرب پویا با کنترل ورودی و حلقه‌های تو در تو

while True:
    user_input = input("تا چه عددی جدول ضرب نمایش داده شود؟ (برای خروج عدد منفی وارد کنید): ")

    # بررسی ورودی غیرعددی
    if not user_input.isdigit() and not (user_input.startswith('-') and user_input[1:].isdigit()):
        print(" لطفاً فقط عدد وارد کنید!")
        continue  # برگرد و دوباره از کاربر بپرس

    n = int(user_input)  # تبدیل رشته به عدد صحیح

    # بررسی منفی بودن عدد
    if n < 0:
        print("⛔ برنامه متوقف شد.")
        break  # خروج از حلقه و پایان برنامه

    print(f"\n جدول ضرب تا عدد {n}:\n")

    # حلقه‌های تو در تو برای تولید جدول ضرب
    for i in range(1, n + 1):
        for j in range(1, n + 1):
            print(f"{i*j:4}", end=" ")  # چاپ نتیجه با فاصله ثابت
        print()  # برای رفتن به خط بعد بعد از هر سطر

    print("\n جدول ضرب با موفقیت نمایش داده شد.\n")